    public void calcular(){
    
        String miUbicacion = (String)ubicacion.getSelectedItem();
        String miDestino = (String)destino.getSelectedItem();

        char miU=EnviarMunicipios(miUbicacion);
        char miD=EnviarMunicipios(miDestino);

        if(Character.compare(miU, miD) == 0){
            JOptionPane.showMessageDialog(null, "Estas en "+ miDestino);
            PanelResult.setVisible(false);
        }else{
        
            String respuesta = principal(miU, miD);

            String[] partes = respuesta.split(":");
            String kms = partes[0]; 
            String iniciales = partes[1]; 
            int dis = Integer.parseInt(kms);
            int horas = 0;

            while(dis>60){
                dis = dis - 60;
                horas++;
            }

            if(dis==0){
                distancia.setText(kms+" km");
                tiempo.setText(horas+" h");
            }else{
                distancia.setText(kms+" km");
                tiempo.setText(horas+" h "+dis+" min");
            }
            PanelResult.setVisible(true);
        }
        
        
        pintar = true;
        repaint();
    }
    
